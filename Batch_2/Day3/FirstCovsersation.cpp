#include <iostream>

using namespace std;

int main() {

    // Output
    // SYNTAX
    // cout << ENTITY_1 << ENTITY_2 .... << ENTITY_N ;

    cout << "     Hello     " << endl ;
    cout << "     Hel" << "lo     " << endl ;

    cout << "3+5" << endl;
    cout << 3+5 << endl;
    cout << 8 << endl;

    cout << "3+5" << endl << 3+5 << endl << 8 << endl;

    cout << endl << endl << endl;

    cout << "Please enter your name: ";

    // SYNTAX FOR DECLARATION OF VARIABLE
    // DATA_TYPE VAR_NAME_1, VAR_NAME_2, ... VAR_NAME_N;

    string name;

    // INPUT SYNTAX
    // cin >> VAR_NAME_1 >> VAR_NAME_2 .... >> VAR_NAME_N;

    cin >> name ;

    // Print "Hello ", name, "! How are you ?"
    cout << "Hello " << name << "! How are you?" << endl;

    cout << endl << endl << endl;

}